import re
from dateutil import parser
import numpy as np

def clean_time(x):
    x = str(x).strip().upper()


    x = x.replace('.', ':')
    x = x.replace('HRS', '')
    x = x.replace(' ', '')

    try:
        return parser.parse(x).time()
    except:
        return np.nan

df['TIME OF OCCURRENCE'] = df['TIME OF OCCURRENCE'].apply(clean_time)
